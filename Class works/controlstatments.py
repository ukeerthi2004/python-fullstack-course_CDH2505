# Example of a for loop in Python

# Loop through a list of numbers
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(f"Number: {number}")

# Loop through a range of numbers
for i in range(1, 6):
    print(f"Range value: {i}")

# Loop through a string
text = "Python"
for char in text:
    print(f"Character: {char}")
    